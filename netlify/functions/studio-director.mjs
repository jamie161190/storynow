export default async (req) => {
  const adminSecret = process.env.ADMIN_SECRET;
  if (!adminSecret) return json({ error: 'Studio not configured' }, 500);
  if (req.headers.get('x-admin-secret') !== adminSecret) return json({ error: 'Unauthorized' }, 401);

  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) return json({ error: 'AI not configured' }, 500);

  let body;
  try { body = await req.json(); } catch { return json({ error: 'Invalid request' }, 400); }

  const { action } = body;

  // ── Analyse uploaded clips/photos ──
  if (action === 'analyse') {
    return handleAnalyse(apiKey, body);
  }

  // ── Generate structured ad builds from analysed assets ──
  if (action === 'concepts') {
    return handleConcepts(apiKey, body);
  }

  return json({ error: 'Unknown action' }, 400);
};

// ── Analyse: Send frames/photos to Claude Vision for auto-naming and tagging ──
async function handleAnalyse(apiKey, { assets }) {
  if (!assets || !assets.length) return json({ error: 'No assets to analyse' }, 400);

  const imageBlocks = [];
  for (let i = 0; i < assets.length; i++) {
    const a = assets[i];
    if (a.isLong && a.frames && a.frames.length) {
      imageBlocks.push({ type: 'text', text: `Asset ${i + 1} (LONG VIDEO, id: ${a.id}, duration: ${a.duration}s). The following ${a.frames.length} frames are sampled across the clip:` });
      for (const f of a.frames) {
        imageBlocks.push({ type: 'image', source: { type: 'base64', media_type: 'image/jpeg', data: f.frame } });
        imageBlocks.push({ type: 'text', text: `Frame at ${Math.floor(f.time / 60)}:${String(Math.floor(f.time % 60)).padStart(2, '0')}` });
      }
    } else {
      imageBlocks.push({ type: 'image', source: { type: 'base64', media_type: 'image/jpeg', data: a.frame } });
      imageBlocks.push({ type: 'text', text: `Asset ${i + 1} (${a.type}, id: ${a.id}):` });
    }
  }

  const hasLongClips = assets.some(a => a.isLong);

  const userContent = [
    ...imageBlocks,
    {
      type: 'text',
      text: `Analyse each asset above. For each one, return a JSON array with objects containing:
- id: the asset id I gave you
- name: a descriptive kebab-case filename (e.g. "chase-hearing-name-reaction", "family-listening-together")
- contentTag: one of: reaction, b-roll, screen-recording, product-demo, behind-the-scenes, testimonial, lifestyle, close-up
- mood: one of: excited, emotional, calm, funny, dramatic, heartwarming, curious
- usability: one of: hook, middle, cta, standalone
- description: one sentence describing what's happening
- marketingPotential: 1-5 rating based on the Storytold-specific criteria I gave you
- bestMoment: describe the single most powerful marketing moment you can see
- suggestedOverlay: text overlay that would make this asset more powerful in an ad (e.g. "She had no idea her daughter's name would be in the story")
- suggestedMockupPair: which mockup screen would pair well after/before this clip (e.g. "child-details" after a reaction clip to show HOW the product works). Use one of: landing-hero, category-select, child-details, voice-select, review-summary, loading, preview-player, full-player, personal-message, share-panel, whatsapp-chat, gift-flow, story-library. Or "none" if the clip stands alone.
- suggestedHook: write the single best hook line for an ad using this footage. Use your marketing brain. Think about what would stop a parent scrolling. It should feel natural, not templated.
${hasLongClips ? `
For LONG VIDEO clips with multiple frames, also include:
- moments: an array of the best 3-5 moments with:
  - timestamp: "M:SS" format
  - time: seconds as number
  - startTime: suggested clip start (seconds)
  - endTime: suggested clip end (seconds)
  - description: what happens at this moment
  - name: kebab-case name for this clipped moment
  - marketingPotential: 1-5 for this specific moment
  - suggestedOverlay: text overlay for this specific moment
  - usability: hook, middle, cta, or standalone
` : ''}
Return ONLY the JSON array, no other text.`
    }
  ];

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01',
        'Content-Type': 'application/json'
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-6',
        max_tokens: 4096,
        system: ANALYSIS_BRAIN,
        messages: [{ role: 'user', content: userContent }]
      })
    });

    if (!response.ok) {
      const err = await response.text();
      console.error('[DIRECTOR] Claude Vision error:', response.status, err);
      return json({ error: 'Analysis failed. Try again.' }, 500);
    }

    const result = await response.json();
    let text = '';
    for (const block of result.content) {
      if (block.type === 'text') text += block.text;
    }

    try {
      const jsonMatch = text.match(/\[[\s\S]*\]/);
      const analyses = jsonMatch ? JSON.parse(jsonMatch[0]) : [];
      return json({ analyses });
    } catch (e) {
      console.error('[DIRECTOR] JSON parse error:', e.message);
      return json({ analyses: [], raw: text });
    }
  } catch (e) {
    console.error('[DIRECTOR] Analysis error:', e.message);
    return json({ error: 'Analysis failed: ' + e.message }, 500);
  }
}

