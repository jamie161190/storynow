// Fallback parser for when Claude returns markdown instead of JSON
function parseFallbackConcepts(text) {
  const sections = text.split(/(?=## Concept \d)/g).filter(s => s.includes('## Concept'));
  return sections.map((section, i) => {
    const lines = section.split('\n');
    const name = (lines[0] || '').replace(/^## Concept \d+:\s*/, '').trim();

    const get = (key) => {
      const line = lines.find(l => l.toLowerCase().includes(key.toLowerCase()));
      return line ? line.split(':').slice(1).join(':').trim().replace(/\*\*/g, '') : '';
    };

    const timelineLines = lines.filter(l => /^\s*-\s*\d/.test(l));
    const timeline = timelineLines.map(l => l.replace(/^\s*-\s*/, '').trim());

    return {
      id: i + 1,
      name,
      platform: get('Platform'),
      format: get('Format'),
      duration: get('Duration'),
      whyItWorks: get('Why it works'),
      music: get('Music'),
      hookText: get('Opening hook'),
      timeline,
      raw: section
    };
  });
}

